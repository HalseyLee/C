#include "yunSuan.h"

/**
 *  加法
 *
 *  @param a <#a description#>
 *  @param b <#b description#>
 *
 *  @return <#return value description#>
 */
int add(int a,int b)
{
    int num = a + b;
    return num;
}
/**
 *  减法
 *
 *  @param a <#a description#>
 *  @param b <#b description#>
 *
 *  @return <#return value description#>
 */
int subtraction(int a,int b)
{
    int num = a - b;
    return num;
}
/**
 *  乘法
 *
 *  @param a <#a description#>
 *  @param b <#b description#>
 *
 *  @return <#return value description#>
 */
int multip(int a,int b)
{
    int num = a * b;
    return num;
}
/**
 *  除法
 *
 *  @param a <#a description#>
 *  @param b <#b description#>
 *
 *  @return <#return value description#>
 */
int division(int a,int b)
{
    int num = a / b;
    return num;
}